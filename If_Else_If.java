public class If_Else_If {
    public static void main(String[] args) {
        int number = 0;

        if (number > 0) {
            System.out.print("The number is positive.");
        } else if (number < 0) {
            System.out.print("The number is negative.");
        } else {
            System.out.print("The number is 0.");
        }
    }
}
