public class BitwiseOperators {

    public static void main(String[] args) {
        // Binary AND
        System.out.println("Binary AND :\n5 & 6 = " + (5 & 6));
        // Binary OR
        System.out.println("Binary OR :\n5 | 6 = " + (5 | 6));
        // Binary XOR
        System.out.println("Binary XOR :\n5 ^ 6 = " + (5 ^ 6));
        // Binary One's Complement
        System.out.println("Binary One's Complement :\n~5 = " + (~5)); // Notes Available.
        // Binary Left Shift
        System.out.println("Binary Left Shift :\n5 << 2 = " + (5 << 2));
        // Binary Right Shift
        System.out.println("Binary Right Shift :\n6 >> 1 = " + (6 >> 1));
    }
}