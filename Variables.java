public class Variables {
    public static void main(String[] args) {
        int a = 3;
        int b = 6;
        System.out.println(a);
        System.out.println(b);
        a = 9;
        System.out.println(a);

        String name = "Vamsi";
        System.out.println(name);

        byte c = 7;
        System.out.println(c);

        boolean var = true;
        System.out.print(var);
    }
}